function nearby(scen::Int64, rank::Int64)::String
    lo = rank - Int64(2)
    if lo < Int64(1)
        lo = Int64(1)
    end
    hi = lo + Int64(4)
    if hi > N_FILM
        hi = N_FILM
        lo = hi - Int64(4)
        if lo < Int64(1)
            lo = Int64(1)
        end
    end
    out = film_at(scen, lo)
    i = lo + Int64(1)
    while i <= hi
        out = out * " || " * film_at(scen, i)
        i = i + Int64(1)
    end
    return out
end

