"""
Exact / near-exact per-film posterior inference.

Given fitted type priors, films factorize once the shared observation noise
`σ_obs` is fixed or marginalized. For fixed decay `η = logit(d)` and `σ_obs`,
`logO` has a conjugate Gaussian posterior and an analytic marginal evidence.

Downstream rankings still require Monte Carlo over composed gross draws.
"""

# ---------------------------------------------------------------------------
# Stable curve primitives (also Wasm-safe)
# ---------------------------------------------------------------------------

"""Opening-to-cumulative multiplier at run-week `t` for retention `d`."""
function curve_factor(d::Float64, t::Float64)::Float64
    t <= 0 && return 0.0
    if abs(d - 1.0) < 1e-12
        return t
    end
    # (1 - d^t) / (1 - d) via expm1 for stability near d≈1
    return -expm1(t * log(d)) / (1 - d)
end

"""Interval gross multiplier from run-week `t0` to `t1` (non-overlapping)."""
function interval_factor(d::Float64, t0::Float64, t1::Float64)::Float64
    t1 <= t0 && return 0.0
    return curve_factor(d, t1) - curve_factor(d, t0)
end

"""Remaining-gross multiplier from banked pin time to cutoff."""
function remaining_factor(d::Float64, t_pin::Float64, t_cut::Float64)::Float64
    # G = banked * (1 + (d^{t_pin} - d^{t_cut}) / (1 - d^{t_pin}))
    # remaining / banked = (d^{t_pin} - d^{t_cut}) / (1 - d^{t_pin})
    t_cut <= t_pin && return 0.0
    d = clamp(d, 1e-9, 1 - 1e-9)
    num = exp(t_pin * log(d)) - exp(t_cut * log(d))
    den = 1 - exp(t_pin * log(d))
    den <= 0 && return 0.0
    return max(num / den, 0.0)
end

logistic(x::Float64) = 1 / (1 + exp(-x))
# logit is defined in model.jl

# Interval observations come from `interval_observations` in data.jl:
# NamedTuple (film, t_start, t_end, interval_gross, theaters_end).

# ---------------------------------------------------------------------------
# Analytic logO conditional + evidence (fixed η, σ)
# ---------------------------------------------------------------------------

"""
For fixed decay `d` and noise `σ`, compute the marginal evidence for interval
observations and the posterior of `logO`.

Each interval contributes:
    log(ΔC) = logO + log(interval_factor(d, t_start, t_end)) + ε,  ε ~ N(0, σ²)

Returns `(log_evidence, post_mean, post_std)`.
"""
function logO_posterior(
    intervals,
    d::Float64,
    σ::Float64,
    μ_logO::Float64,
    σ_logO::Float64,
)
    σ = max(σ, 1e-6)
    σ_logO = max(σ_logO, 1e-6)
    zs = Float64[]
    for iv in intervals
        f = interval_factor(d, Float64(iv.t_start), Float64(iv.t_end))
        f > 0 || continue
        iv.interval_gross > 0 || continue
        push!(zs, log(Float64(iv.interval_gross)) - log(f))
    end
    K = length(zs)
    if K == 0
        # No observations → prior is posterior; evidence = 1
        return (0.0, μ_logO, σ_logO)
    end
    τ2 = σ_logO^2
    σ2 = σ^2
    # Posterior precision / mean
    prec = 1 / τ2 + K / σ2
    post_var = 1 / prec
    post_mean = post_var * (μ_logO / τ2 + sum(zs) / σ2)
    post_std = sqrt(post_var)
    # Marginal evidence: z ~ N(μ 1, σ²I + τ² 11ᵀ)
    r = zs .- μ_logO
    Q = sum(abs2, r)
    S = sum(r)
    logdet = K * log(σ2) + log1p(K * τ2 / σ2)
    quad = Q / σ2 - (τ2 / (σ2 * (σ2 + K * τ2))) * S * S
    logZ = -0.5 * (K * log(2π) + logdet + quad)
    return (logZ, post_mean, post_std)
end

