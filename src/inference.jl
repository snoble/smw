const GL_NODES_16, GL_WEIGHTS_16 = _gl32()

"""Map Gauss–Hermite nodes for η ~ N(μ, σ²): η = μ + √2 σ x, weight *= √π⁻¹ factor handled via w*π^{-1/2}."""
function hermite_nodes(μ::Float64, σ::Float64; order::Int = 16)
    xs = GH_NODES_16
    ws = GH_WEIGHTS_16
    n = min(order, length(xs))
    # ∫ f(η) N(η;μ,σ) dη = ∫ f(μ+√2 σ x) * w(x) / √π  dx  where w are GH weights for e^{-x²}
    inv_sqrt_pi = 1 / sqrt(π)
    nodes = Vector{Float64}(undef, n)
    weights = Vector{Float64}(undef, n)
    for i in 1:n
        nodes[i] = μ + sqrt(2) * σ * xs[i]
        weights[i] = ws[i] * inv_sqrt_pi
    end
    return nodes, weights
end

"""Gauss–Legendre nodes on truncated σ ∈ [lo, hi] with truncated-normal prior density later."""
function legendre_nodes(lo::Float64, hi::Float64; order::Int = 16)
    xs = GL_NODES_16
    ws = GL_WEIGHTS_16
    n = min(order, length(xs))
    nodes = Vector{Float64}(undef, n)
    weights = Vector{Float64}(undef, n)
    mid = 0.5 * (hi + lo)
    half = 0.5 * (hi - lo)
    for i in 1:n
        nodes[i] = mid + half * xs[i]
        weights[i] = ws[i] * half
    end
    return nodes, weights
end

"""Unnormalized truncated-normal log-density on [lo,hi] (normalization handled across grid)."""
function log_trunc_normal(x::Float64, μ::Float64, σ::Float64)
    z = (x - μ) / σ
    return -0.5 * z * z - log(σ) - 0.5 * log(2π)
end

