"""Kendall-tau style distance between two Top-10 title lists (positions matter)."""
function rank_list_distance(a::AbstractVector{<:AbstractString}, b::AbstractVector{<:AbstractString})
    pos_b = Dict(t => i for (i, t) in enumerate(b))
    dist = 0.0
    for (i, t) in enumerate(a)
        j = get(pos_b, t, length(b) + 2)
        dist += abs(i - j)
    end
    # Penalize titles in b missing from a
    set_a = Set(a)
    for (j, t) in enumerate(b)
        if !(t in set_a)
            dist += abs(j - (length(a) + 2))
        end
    end
    return dist
end

"""
Among draws where `player` wins (sole or tied), pick the draw whose Top-10 is
nearest the coordinate-wise consensus ranking (modal/median position proxy:
ranking from median grosses among win draws).
"""
function representative_win_draw(
    titles::Vector{String},
    G::Matrix{Float64},
    win_draw_indices::Vector{Int};
    top_n::Int = 10,
)
    isempty(win_draw_indices) && return nothing
    n_films = size(G, 1)
    medG = [median(@view G[i, win_draw_indices]) for i in 1:n_films]
    consensus = rank_by_gross(titles, medG; n = top_n)
    best = win_draw_indices[1]
    best_d = Inf
    for j in win_draw_indices
        ranking = rank_by_gross(titles, @view(G[:, j]); n = top_n)
        d = rank_list_distance(ranking, consensus)
        if d < best_d
            best_d = d
            best = j
        end
    end
    return best
end

"""Build `FilmPrior` vector from season data + fitted type priors."""
function film_priors_from_season(season::SeasonData, type_priors::AbstractDict)
    floors = observed_floors(season)
    times = observed_times(season)
    priors = FilmPrior[]
    for f in season.films
        t_cut = t_cutoff(f.release_date)
        banked = get(floors, f.title, 0.0)
        t_now = get(times, f.title, 0.0)
        # Latest theaters from observations
        theaters = missing
        for o in season.observations
            if o.film == f.title && (theaters isa Missing || o.t >= t_now - 1e-9)
                theaters = o.theaters
            end
        end
        if f.released
            p = prior_for(f.type, type_priors)
            μ_logO = Float64(p.μ_logO)
            σ_logO = Float64(p.σ_logO)
            # Synthetic EB history often collapses unknown types to pooled long legs.
            # Prefer the curated type decay table when this type wasn't actually fit.
            if !haskey(type_priors, f.type) && haskey(UNRELEASED_DECAY_D, f.type)
                d = clamp(UNRELEASED_DECAY_D[f.type], 0.05, 0.95)
                μ_logit_d = logit(d)
                σ_logit_d = UNRELEASED_LOGIT_D_STD
            else
                μ_logit_d = Float64(p.μ_logit_d)
                σ_logit_d = Float64(p.σ_logit_d)
            end
        else
            up = unreleased_prior(f)
            μ_logO = up.μ_logO
            σ_logO = up.σ_logO
            μ_logit_d = up.μ_logit_d
            σ_logit_d = up.σ_logit_d
        end
        push!(
            priors,
            FilmPrior(
                f.title,
                μ_logO,
                σ_logO,
                μ_logit_d,
                σ_logit_d,
                t_cut,
                banked,
                t_now,
                theaters,
                f.released,
            ),
        )
    end
    return priors
end
