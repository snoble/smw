"""
Calibrated remaining-gross prior from exhibition state.

When theaters are very low and PTA is weak, remaining upside collapses.
Returns an optional soft observation on `log(remaining)` ≈ logO + log(remaining_factor).
"""
function theater_tail_observation(
    banked::Float64,
    theaters::Union{Missing,Int},
    t_now::Float64,
    t_cut::Float64;
    pta::Union{Missing,Float64} = missing,
)
    (banked > 0 && t_cut > t_now && t_now > 0) || return nothing
    th = theaters isa Missing ? 2000 : Int(theaters)
    # Expected remaining weeks of meaningful exhibition
    if th <= 50
        remaining_weeks = 0.5
        σ_tail = 0.25
    elseif th <= 200
        remaining_weeks = 1.0
        σ_tail = 0.35
    elseif th <= 500
        remaining_weeks = 1.5
        σ_tail = 0.45
    else
        return nothing  # still wide — no extra constraint
    end
    weekly = if pta isa Missing
        # Rough: banked/t_now as average weekly, scaled by theater share vs wide (~3500)
        (banked / max(t_now, 1.0)) * (th / 3500)
    else
        Float64(pta) * th
    end
    expected_remaining = max(weekly * remaining_weeks, 1.0)
    return (log_remaining = log(expected_remaining), σ = σ_tail)
end

# ---------------------------------------------------------------------------
# Per-film quadrature posterior over η | σ
# ---------------------------------------------------------------------------

struct FilmPrior
    title::String
    μ_logO::Float64
    σ_logO::Float64
    μ_logit_d::Float64
    σ_logit_d::Float64
    t_cut::Float64
    banked::Float64
    t_now::Float64
    theaters::Union{Missing,Int}
    released::Bool
end

"""
For one film and fixed σ, return discrete posterior over Hermite η-nodes:
`(η_nodes, weights_normalized, logO_mean_per_node, logO_std_per_node, log_evidence)`.
"""
function film_eta_posterior(
    prior::FilmPrior,
    intervals,
    σ::Float64;
    order::Int = 16,
)
    ηs, w0 = hermite_nodes(prior.μ_logit_d, prior.σ_logit_d; order)
    n = length(ηs)
    logw = fill(-Inf, n)
    μO = Vector{Float64}(undef, n)
    σO = Vector{Float64}(undef, n)
    film_iv = filter(iv -> iv.film == prior.title, intervals)
    for i in 1:n
        d = clamp(logistic(ηs[i]), 1e-6, 1 - 1e-6)
        logZ, m, s = logO_posterior(film_iv, d, σ, prior.μ_logO, prior.σ_logO)
        # Optional theater-tail soft observation on remaining
        tail = theater_tail_observation(prior.banked, prior.theaters, prior.t_now, prior.t_cut)
        if tail !== nothing && prior.banked > 0
            # remaining = banked * remaining_factor(d, t_pin, t_cut)
            # so log(remaining) = log(banked) + log(rf)  — independent of logO when pinned
            # Use as soft check on decay instead: predicted remaining vs observed expectation
            t_pin = max(prior.t_now, 1.0)
            rf = remaining_factor(d, t_pin, prior.t_cut)
            pred = prior.banked * rf
            if pred > 0
                resid = log(pred) - tail.log_remaining
                logZ -= 0.5 * (resid / tail.σ)^2 + log(tail.σ) + 0.5 * log(2π)
            end
        end
        logw[i] = log(w0[i]) + logZ
        μO[i] = m
        σO[i] = s
    end
    mlog = maximum(logw)
    w = exp.(logw .- mlog)
    Z = sum(w)
    w ./= Z
    log_evidence = mlog + log(Z)
    return (ηs = ηs, w = w, μO = μO, σO = σO, log_evidence = log_evidence)
end

# ---------------------------------------------------------------------------
# Shared σ marginalization + joint sampling
# ---------------------------------------------------------------------------

struct InferenceConfig
    σ_lo::Float64
    σ_hi::Float64
    σ_prior_μ::Float64
    σ_prior_σ::Float64
    η_order::Int
    σ_order::Int
end

const DEFAULT_INFERENCE = InferenceConfig(0.02, 1.0, 0.15, 0.1, 16, 16)

"""
Build the discrete posterior over σ nodes by multiplying per-film evidences
with the truncated-normal prior.
"""
function sigma_posterior(
    priors::AbstractVector{FilmPrior},
    intervals,
    cfg::InferenceConfig = DEFAULT_INFERENCE,
)
    σs, wσ = legendre_nodes(cfg.σ_lo, cfg.σ_hi; order = cfg.σ_order)
    nσ = length(σs)
    logp = Vector{Float64}(undef, nσ)
    # Cache per (film, σ-index) eta posteriors
    cache = Dict{Tuple{Int,Int},Any}()
    for (j, σ) in enumerate(σs)
        lp = log_trunc_normal(σ, cfg.σ_prior_μ, cfg.σ_prior_σ) + log(wσ[j])
        for (fi, pr) in enumerate(priors)
            if !pr.released
                continue  # unreleased: evidence = 1 (prior only)
            end
            post = film_eta_posterior(pr, intervals, σ; order = cfg.η_order)
            cache[(fi, j)] = post
            lp += post.log_evidence
        end
        logp[j] = lp
    end
    m = maximum(logp)
    w = exp.(logp .- m)
    w ./= sum(w)
    return (σs, w, cache)
end

"""
Draw `n_draws` season-gross matrices using quadrature posterior + exact conditionals.

Returns `G` (films × draws), plus diagnostic NamedTuple.
"""
function sample_factored_grosses(
    priors::AbstractVector{FilmPrior},
    intervals,
    n_draws::Int;
    cfg::InferenceConfig = DEFAULT_INFERENCE,
    seed::Int = 42,
    overrides::Dict{String,Float64} = Dict{String,Float64}(),  # title => opening $M
)
    Random.seed!(seed)
    σs, wσ, cache = sigma_posterior(priors, intervals, cfg)
    n_films = length(priors)
    G = Matrix{Float64}(undef, n_films, n_draws)
    # Precompute CDF for σ
    cdf_σ = cumsum(wσ)
    for draw in 1:n_draws
        # Sample shared σ
        u = rand()
        jσ = searchsortedfirst(cdf_σ, u)
        jσ = clamp(jσ, 1, length(σs))
        σ = σs[jσ]
        for (fi, pr) in enumerate(priors)
            # Opening override shifts μ_logO for controlled unreleased films
            μ_logO = pr.μ_logO
            σ_logO = pr.σ_logO
            if haskey(overrides, pr.title)
                μ_logO = log(overrides[pr.title] * 1e6)
                # keep film's σ_logO (caller may pass tighter)
            end
            if !pr.released
                # Exact prior draw
                logO = μ_logO + σ_logO * randn()
                η = pr.μ_logit_d + pr.σ_logit_d * randn()
                d = clamp(logistic(η), 1e-6, 1 - 1e-6)
                O = exp(logO)
                G[fi, draw] = cumulative_gross(O, d, pr.t_cut)
                continue
            end
            key = (fi, jσ)
            if !haskey(cache, key)
                # Recompute with possibly overridden prior means — rare for released
                pr2 = FilmPrior(
                    pr.title, μ_logO, σ_logO, pr.μ_logit_d, pr.σ_logit_d,
                    pr.t_cut, pr.banked, pr.t_now, pr.theaters, pr.released,
                )
                cache[key] = film_eta_posterior(pr2, intervals, σ; order = cfg.η_order)
            end
            ηs, wη, μO, σO = cache[key].ηs, cache[key].w, cache[key].μO, cache[key].σO
            # Sample η node
            cdf = cumsum(wη)
            k = searchsortedfirst(cdf, rand())
            k = clamp(k, 1, length(ηs))
            η = ηs[k]
            d = clamp(logistic(η), 1e-6, 1 - 1e-6)
            logO = μO[k] + σO[k] * randn()
            O = exp(logO)
            G[fi, draw] = season_total(O, d, pr.t_cut; banked = pr.banked, t_now = pr.t_now)
        end
    end
    return G, (; σs, wσ, n_draws, seed, cfg)
end

